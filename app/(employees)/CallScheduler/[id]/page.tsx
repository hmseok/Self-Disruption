// ── 더보기 메뉴 아이템 ─────────────────────────────────────────────
function MenuItem({
  icon, label, sub, onClick, href, active, disabled, tone,
}: {
  icon: string
  label: string
  sub?: string
  onClick?: () => void
  href?: string
  active?: boolean
  disabled?: boolean
  tone?: 'success' | 'danger'
}) {
  const color = tone === 'danger' ? COLORS.danger
    : tone === 'success' ? COLORS.success
    : active ? COLORS.info
    : COLORS.textPrimary
  const bg = active ? COLORS.bgBlue : 'transparent'
  const hoverBg = tone === 'danger' ? COLORS.bgRed
    : tone === 'success' ? COLORS.bgGreen
    : COLORS.bgGray
  const content = (
    <>
      <span style={{ fontSize: 14, width: 18, textAlign: 'center' }}>{icon}</span>
      <div style={{ flex: 1 }}>
        <div style={{ fontSize: 12, fontWeight: 700, color }}>{label}</div>
        {sub && <div style={{ fontSize: 10, color: COLORS.textMuted }}>{sub}</div>}
      </div>
    </>
  )
  const baseStyle: React.CSSProperties = {
    display: 'flex', alignItems: 'center', gap: 8,
    padding: '6px 10px', borderRadius: 6,
    background: bg,
    border: 'none', cursor: disabled ? 'not-allowed' : 'pointer',
    opacity: disabled ? 0.5 : 1,
    width: '100%', textAlign: 'left',
    textDecoration: 'none',
    transition: 'background 0.15s',
  }
  if (href) {
    return (
      <Link href={href} style={baseStyle}
            onMouseEnter={(e) => { e.currentTarget.style.background = hoverBg }}
            onMouseLeave={(e) => { e.currentTarget.style.background = bg }}>
        {content}
      </Link>
    )
  }
  return (
    <button type="button" onClick={onClick} disabled={disabled} style={baseStyle}
            onMouseEnter={(e) => { if (!disabled) e.currentTarget.style.background = hoverBg }}
            onMouseLeave={(e) => { e.currentTarget.style.background = bg }}>
      {content}
    </button>
  )
}

/**
 * 카테고리 모드에 따른 "표시용 병합 그룹 목록" 생성
 * = 표준 CATEGORIES 또는 DISPLAY_CATEGORIES + 커스텀 카테고리(중복 group 제외)
 *
 * upload/page.tsx의 5곳에서 동일한 expression이 반복되던 것을 이 함수로 통일.
 *
 * @param mode  'display' 또는 그 외 (= 회계 모드)
 * @param customCategories 사용자 커스텀 카테고리 (그룹명으로 중복 판정)
 */
export function getMergedCategoryGroups(
  mode: 'display' | 'accounting' | string,
  customCategories: Array<{ group: string; items: string[] }> = []
) {
  const base = mode === 'display' ? DISPLAY_CATEGORIES : CATEGORIES
  const baseGroups = new Set(base.map(g => g.group))
  const extra = customCategories
    .filter(c => !baseGroups.has(c.group))
    .map(c => ({ group: c.group, items: c.items.map(i => ({ label: i })) }))
  return [...base, ...extra]
}


// 런타임 검증 헬퍼 — 개발 환경에서 카테고리와 매핑 불일치 감지
// (프로덕션에서는 no-op)
export function validateCategoryMaps(): string[] {
  const errors: string[] = []
  const mapKeys = new Set(Object.keys(CATEGORY_RELATED_MAP))
  const catItems = new Set(ALL_CATEGORIES)
  // 고아 키 (MAP에만 있고 CATEGORIES에 없음)
  for (const k of mapKeys) {
    if (!catItems.has(k)) errors.push(`[orphan] CATEGORY_RELATED_MAP 키 '${k}'가 CATEGORIES에 없음`)
  }
  // 누락 (CATEGORIES에 있지만 MAP에 없음 — 경고 수준)
  for (const item of catItems) {
    if (!mapKeys.has(item)) errors.push(`[missing] CATEGORIES '${item}'의 연결 매핑 미정의`)
  }
  return errors
}


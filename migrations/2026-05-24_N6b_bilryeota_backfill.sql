-- ── STEP 1 — 빈 컬럼 채움 (COALESCE 가드) ──
UPDATE fmi_rentals f
  JOIN _n6_backfill b
    ON REPLACE(f.vehicle_car_number,' ','') = b.v
   AND f.dispatch_date = b.d
   AND REPLACE(COALESCE(f.customer_car_number,''),' ','') = COALESCE(b.c,'')
   SET f.repair_factory    = COALESCE(f.repair_factory, b.repair_factory),
       f.customer_birth    = COALESCE(f.customer_birth, b.customer_birth),
       f.dispatch_location = COALESCE(f.dispatch_location, b.dispatch_location),
       f.updated_at = NOW()
 WHERE f.fleet_group = '빌려타';

